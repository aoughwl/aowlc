proc declare(t: Node; name: string): string

proc findTag(n: Node; tag: string): Node =
  if not isList(n): return nil
  for k in n.kids:
    if isList(k) and k.tag == tag: return k
  return nil

proc indexOfKid(n: Node; target: Node): int =
  for i in 0 ..< n.kids.len:
    if n.kids[i] == target: return i
  return -1

proc paramType(paramNode: Node): Node =
  # (param name pragmas type [default])
  var i = 1
  while i < paramNode.kids.len:
    let k = paramNode.kids[i]
    if isList(k) or (isAtom(k) and k.atom != "." and not k.def): return k
    inc i
  return paramNode.kids[2]

# ---------------------------------------------------------------------------
# types
# ---------------------------------------------------------------------------
proc bitsOf(t: Node): string =
  if t.kids.len == 0: return ""
  let last = t.kids[t.kids.len - 1]
  if isAtom(last) and isIntLit(last.atom):
    if last.atom == "-1": return ""
    return last.atom
  return ""

proc genProcTypePtr(t: Node): string =
  let params = findTag(t, "params")
  var ret: Node = nil
  if params != nil:
    let idx = indexOfKid(t, params)
    if idx >= 0 and idx + 1 < t.kids.len: ret = t.kids[idx + 1]
  else:
    if t.kids.len > 0: ret = t.kids[t.kids.len - 1]
  var args: seq[string] = @[]
  if params != nil:
    for k in params.kids:
      if isList(k) and k.tag == "param": args.add genType(paramType(k))
  let argStr = if args.len > 0: joinSeq(args, ", ") else: "void"
  return genType(ret) & "(*)(" & argStr & ")"

proc genType(t: Node): string =
  if isDot(t): return "void"
  if isAtom(t):
    if t.atom == "void": return "void"
    return typeName(t.atom)
  if not isList(t):
    fail("aowlc: bad type node")
    return ""
  let tg = t.tag
  if tg == "i": return "NI" & bitsOf(t)
  elif tg == "u": return "NU" & bitsOf(t)
  elif tg == "f": return "NF" & bitsOf(t)
  elif tg == "c": return "NC" & bitsOf(t)
  elif tg == "bool": return "NB8"
  elif tg == "void": return "void"
  elif tg == "ptr" or tg == "aptr": return genType(t.kids[0]) & "*"
  elif tg == "flexarray": return genType(t.kids[0]) & "*"
  elif tg == "proctype": return genProcTypePtr(t)
  elif tg == "array": return genType(t.kids[0]) & "*"
  else:
    if t.kids.len > 0: return genType(t.kids[0])
    fail("aowlc: unsupported type '" & tg & "'")
    return ""

proc declare(t: Node; name: string): string =
  if isList(t) and t.tag == "array":
    let size = t.kids[t.kids.len - 1]
    let len2 = if isAtom(size): size.atom else: "0"
    return genType(t.kids[0]) & " " & name & "[" & len2 & "]"
  if isList(t) and t.tag == "flexarray":
    return genType(t.kids[0]) & " " & name & "[]"
  if isList(t) and t.tag == "proctype":
    let params = findTag(t, "params")
    var ret: Node = nil
    if params != nil:
      let idx = indexOfKid(t, params)
      if idx >= 0 and idx + 1 < t.kids.len: ret = t.kids[idx + 1]
    else:
      if t.kids.len > 0: ret = t.kids[t.kids.len - 1]
    var args: seq[string] = @[]
    if params != nil:
      for k in params.kids:
        if isList(k) and k.tag == "param": args.add genType(paramType(k))
    let argStr = if args.len > 0: joinSeq(args, ", ") else: "void"
    return genType(ret) & " (*" & name & ")(" & argStr & ")"
  return genType(t) & " " & name

