# ---------------------------------------------------------------------------
# AST node (mirrors aowlc.js's readNif node shape)
# ---------------------------------------------------------------------------
type
  NKind = enum nkAtom, nkStr, nkChar, nkList
  Node = ref object
    kind: NKind
    tag: string          # list tag
    kids: seq[Node]
    atom: string         # atom text
    def: bool
    strVal: string       # string literal
    chrVal: int          # char literal code

proc isList(n: Node): bool = n != nil and n.kind == nkList
proc isAtom(n: Node): bool = n != nil and n.kind == nkAtom
proc isDot(n: Node): bool = isAtom(n) and n.atom == "."

proc fail(msg: string) =
  write stderr, msg & "\n"
  quit 1

# ---------------------------------------------------------------------------
# small string utilities (avoid depending on strutils subset surprises)
# ---------------------------------------------------------------------------
proc myRepeat(s: string; n: int): string =
  result = ""
  var i = 0
  while i < n:
    result.add s
    inc i

proc joinSeq(xs: seq[string]; sep: string): string =
  result = ""
  var first = true
  for x in xs:
    if not first: result.add sep
    first = false
    result.add x

proc splitLinesNl(s: string): seq[string] =
  result = @[]
  var cur = ""
  for ch in s:
    if ch == '\n':
      result.add cur
      cur = ""
    else:
      cur.add ch
  result.add cur

proc stripBoth(s: string): string =
  var a = 0
  var b = s.len - 1
  while a <= b and (s[a] == ' ' or s[a] == '\t' or s[a] == '\r' or s[a] == '\n'):
    inc a
  while b >= a and (s[b] == ' ' or s[b] == '\t' or s[b] == '\r' or s[b] == '\n'):
    dec b
  if a > b: return ""
  return s[a .. b]

proc beforeDot(s: string): string =
  var i = 0
  while i < s.len and s[i] != '.': inc i
  return s[0 ..< i]

proc oneOf(s: string; xs: openArray[string]): bool =
  for x in xs:
    if x == s: return true
  return false

proc toOctal(n: int): string =
  if n == 0: return "0"
  var v = n
  result = ""
  while v > 0:
    var d = ""
    d.add chr(ord('0') + (v mod 8))
    result = d & result
    v = v div 8

# ---------------------------------------------------------------------------
# literal detection / rendering (faithful to aowlc.js regexes)
# ---------------------------------------------------------------------------
proc isIntLit(a: string): bool =
  if a.len == 0: return false
  var i = 0
  if a[0] == '-': i = 1
  if i >= a.len: return false
  while i < a.len:
    if a[i] < '0' or a[i] > '9': return false
    inc i
  return true

proc isUIntLit(a: string): bool =
  if a.len == 0: return false
  var i = 0
  if a[i] < '0' or a[i] > '9': return false
  while i < a.len and a[i] >= '0' and a[i] <= '9': inc i
  if i >= a.len or a[i] != 'u': return false
  inc i
  if i == a.len: return true
  if a[i] == 'l':
    inc i
    if i == a.len: return true
    if a[i] == 'l':
      inc i
      return i == a.len
    return false
  return false

proc isFloatLit(a: string): bool =
  if a.len == 0: return false
  var i = 0
  if a[i] == '-': inc i
  var sawDigit = false
  while i < a.len and a[i] >= '0' and a[i] <= '9':
    inc i
    sawDigit = true
  if i < a.len and a[i] == '.':
    inc i
    while i < a.len and a[i] >= '0' and a[i] <= '9':
      inc i
      sawDigit = true
    if not sawDigit: return false
  else:
    if not sawDigit: return false
  if i < a.len and (a[i] == 'e' or a[i] == 'E'):
    inc i
    if i < a.len and (a[i] == '+' or a[i] == '-'): inc i
    if i >= a.len: return false
    var esaw = false
    while i < a.len and a[i] >= '0' and a[i] <= '9':
      inc i
      esaw = true
    if not esaw: return false
  if i != a.len: return false
  for ch in a:
    if ch == '.' or ch == 'e' or ch == 'E': return true
  return false

proc parseI64(s: string): int64 =
  var i = 0
  var neg = false
  if i < s.len and s[i] == '-':
    neg = true
    inc i
  var v: int64 = 0
  while i < s.len and s[i] >= '0' and s[i] <= '9':
    v = v * 10 + int64(ord(s[i]) - ord('0'))
    inc i
  if neg: return -v
  return v

proc parseU64(s: string): uint64 =
  var i = 0
  var v: uint64 = 0
  while i < s.len and s[i] >= '0' and s[i] <= '9':
    v = v * 10'u64 + uint64(ord(s[i]) - ord('0'))
    inc i
  return v

proc genIntLit(a: string): string =
  if a == "-9223372036854775808": return "(-9223372036854775807LL - 1LL)"
  let v = parseI64(a)
  if v >= -2147483648'i64 and v <= 2147483647'i64: return $v
  return $v & "LL"

proc genUIntLit(a: string): string =
  var n = a.len
  while n > 0 and (a[n-1] == 'l' or a[n-1] == 'L'): dec n
  if n > 0 and (a[n-1] == 'u' or a[n-1] == 'U'): dec n
  let digits = a[0 ..< n]
  let v = parseU64(digits)
  if v <= 4294967295'u64: return $v & "u"
  return $v & "ull"

# ---------------------------------------------------------------------------
# name mangling (faithful port of lengc/mangler.nim mangleToC)
# ---------------------------------------------------------------------------
proc escapeChar(ch: char): string =
  const HEX = "0123456789ABCDEF"
  let nn = ord(ch)
  result = "X"
  result.add HEX[(nn shr 4) and 0xF]
  result.add HEX[nn and 0xF]
  result.add 'Q'

proc mangleToC(s: string): string =
  result = ""
  var i = 0
  while i < s.len:
    let c = s[i]
    if (c >= 'A' and c <= 'P') or (c >= 'R' and c <= 'Z') or
       (c >= 'a' and c <= 'z') or (c >= '0' and c <= '9'):
      result.add c
    elif c == 'Q': result.add "QQ"
    elif c == '_': result.add "Q_"
    elif c == '.': result.add "_"
    elif c == '[':
      if i + 1 < s.len and s[i+1] == ']':
        if i + 2 < s.len and s[i+2] == '=':
          result.add "putQ"
          i += 2
        else:
          result.add "getQ"
          i += 1
      else:
        result.add escapeChar(c)
    elif c == '=':
      if i + 1 < s.len and s[i+1] == '=':
        result.add "eqQ"
        inc i
      else:
        result.add "eQ"
    elif c == '<':
      if i + 1 < s.len and s[i+1] == '=':
        result.add "leQ"
        inc i
      else:
        result.add "ltQ"
    elif c == '>':
      if i + 1 < s.len and s[i+1] == '=':
        result.add "geQ"
        inc i
      else:
        result.add "gtQ"
    elif c == '$': result.add "dollarQ"
    elif c == '%': result.add "percentQ"
    elif c == '&': result.add "ampQ"
    elif c == '^': result.add "roofQ"
    elif c == '!': result.add "emarkQ"
    elif c == '?': result.add "qmarkQ"
    elif c == '*': result.add "starQ"
    elif c == '+': result.add "plusQ"
    elif c == '-': result.add "minusQ"
    elif c == '/': result.add "slashQ"
    elif c == '\\': result.add "bslashQ"
    elif c == '~': result.add "tildeQ"
    elif c == ':': result.add "colonQ"
    elif c == '@': result.add "atQ"
    elif c == '|': result.add "barQ"
    else: result.add escapeChar(c)
    inc i

proc makeCString(s: string): string =
  result = "\""
  for ch in s:
    let code = ord(ch)
    if (code >= 0 and code <= 0x1F) or code >= 0x7F:
      result.add "\\"
      result.add toOctal(code)
    elif ch == '\'' or ch == '"' or ch == '\\' or ch == '?':
      result.add "\\"
      result.add ch
    else:
      result.add ch
  result.add "\""

proc toCCharLit(code: int): string =
  if (code >= 0 and code <= 0x1F) or code >= 0x7F:
    return "'\\" & toOctal(code) & "'"
  let ch = chr(code)
  if ch == '\'' or ch == '"' or ch == '\\' or ch == '?':
    return "'\\" & ch & "'"
  return "'" & ch & "'"

# ---------------------------------------------------------------------------
# operator tables
# ---------------------------------------------------------------------------
proc typedBinOp(t: string): string =
  if t == "add": return " + "
  elif t == "sub": return " - "
  elif t == "mul": return " * "
  elif t == "div": return " / "
  elif t == "mod": return " % "
  elif t == "shl": return " << "
  elif t == "shr": return " >> "
  elif t == "ashr": return " >> "
  elif t == "bitand": return " & "
  elif t == "bitor": return " | "
  elif t == "bitxor": return " ^ "
  return ""

proc cmpBinOp(t: string): string =
  if t == "eq": return " == "
  elif t == "neq": return " != "
  elif t == "le": return " <= "
  elif t == "lt": return " < "
  elif t == "ge": return " >= "
  elif t == "gt": return " > "
  elif t == "and": return " && "
  elif t == "or": return " || "
  return ""

proc suffixType(s: string): string =
  if s == "i64": return "NI64"
  elif s == "i32": return "NI32"
  elif s == "i16": return "NI16"
  elif s == "i8": return "NI8"
  elif s == "u64": return "NU64"
  elif s == "u32": return "NU32"
  elif s == "u16": return "NU16"
  elif s == "u8": return "NU8"
  elif s == "f64": return "NF64"
  elif s == "f32": return "NF32"
  elif s == "f": return "NF64"
  return "NI64"

