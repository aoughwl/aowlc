# build an AST from the streaming cursor
# ---------------------------------------------------------------------------
# On-disk own-module symbols use an empty trailing hash slot (`fib.1.`); the
# loader (nifreader) expands that empty slot to the module suffix
# (`fib.1.fib`). The oracle reads the RAW on-disk form, so strip the suffix the
# loader appended to recover it — matching mangleToC's input byte-for-byte.
proc stripModuleSuffix(s: string): string =
  ## Nimony's real mangling KEEPS the module suffix: the loader (nifreader)
  ## expands an own-module symbol's empty trailing hash slot (`ini.0.`) to the
  ## module basename (`ini.0.fibdncrrv`), which is exactly what nimony's own C
  ## backend emits. Stripping it made a symbol's definition (`X60Qini_0_`)
  ## disagree with cross-module uses (`X60Qini_0_fibdncrrv`) and collide across
  ## modules, so we keep the fully-qualified name verbatim.
  result = s

proc buildNode(c: var Cursor): Node =
  case c.kind
  of ParLe:
    result = Node(kind: nkList, tag: pool.tags[c.tagId])
    inc c
    while c.kind != ParRi:
      result.kids.add buildNode(c)
    consumeParRi c
  of ParRi:
    inc c
    result = nil
  of DotToken:
    result = Node(kind: nkAtom, atom: ".")
    inc c
  of Symbol:
    result = Node(kind: nkAtom, atom: stripModuleSuffix(pool.syms[c.symId]))
    inc c
  of SymbolDef:
    result = Node(kind: nkAtom, atom: stripModuleSuffix(pool.syms[c.symId]), def: true)
    inc c
  of Ident:
    result = Node(kind: nkAtom, atom: pool.strings[c.litId])
    inc c
  of IntLit:
    result = Node(kind: nkAtom, atom: $pool.integers[c.intId])
    inc c
  of UIntLit:
    result = Node(kind: nkAtom, atom: $pool.uintegers[c.uintId] & "u")
    inc c
  of FloatLit:
    result = Node(kind: nkAtom, atom: $pool.floats[c.floatId])
    inc c
  of StringLit:
    result = Node(kind: nkStr, strVal: pool.strings[c.litId])
    inc c
  of CharLit:
    result = Node(kind: nkChar, chrVal: ord(c.charLit))
    inc c
  else:
    inc c
    result = nil

proc emitModuleBody*(root: var Cursor): string =
  resetEmitter()
  let tree = buildNode(root)
  result = emitUnit(classify(tree))
