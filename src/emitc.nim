type
  GlobalInfo = object
    name: string
    decl: string
    nameAtom: string
    needsInit: bool
    value: Node

proc genGlobal(g: Node): GlobalInfo =
  let nameAtom = g.kids[0]
  let pragmas = g.kids[1]
  let typ = g.kids[2]
  let value = if g.kids.len > 3: g.kids[3] else: nil
  let nm = declName(nameAtom.atom, pragmas)
  let isConst = g.tag == "const"
  var decl = declare(typ, nm)
  if isConst: decl = "static const " & decl
  let hasInit = value != nil and not isDot(value) and isLiteralNode(value)
  result = default(GlobalInfo)
  result.name = nm
  result.decl = decl & (if hasInit: " = " & genExpr(value) else: "") & ";"
  result.nameAtom = nameAtom.atom
  result.needsInit = value != nil and not isDot(value) and not hasInit
  result.value = value

