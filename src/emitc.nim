## emitc — nimony port of aowlc.js: post-hexer `.c.nif` → C. aowlc.js is the
## byte-identical ORACLE; emitjs.nim (~/aifjs/src) is the nifcursors API template.
##
## aowlc.js is a two-pass classify+emit design that relies on random access over
## an in-memory node tree. So this port first builds an AST (`Node`) from the
## streaming `Cursor`, then ports aowlc.js's logic directly against that AST.
when defined(nimony): {.feature: "lenientnils".}
