type
  ProcParts = object
    nameAtom: Node
    params: Node
    ret: Node
    body: Node
    pragmas: Node
    pnodes: seq[Node]

proc procParts(p: Node): ProcParts =
  result = default(ProcParts)
  result.nameAtom = p.kids[0]
  result.params = findTag(p, "params")
  result.ret = nil
  if result.params != nil:
    let pi = indexOfKid(p, result.params)
    if pi + 1 < p.kids.len: result.ret = p.kids[pi + 1]
  else:
    if p.kids.len > 1: result.ret = p.kids[1]
  result.body = nil
  var j = p.kids.len - 1
  while j >= 0:
    if isList(p.kids[j]) and p.kids[j].tag == "stmts":
      result.body = p.kids[j]
      break
    dec j
  result.pragmas = findTag(p, "pragmas")
  result.pnodes = @[]
  if result.params != nil:
    for k in result.params.kids:
      if isList(k) and k.tag == "param": result.pnodes.add k

proc procSignature(p: Node): string =
  let pp = procParts(p)
  let name = declName(pp.nameAtom.atom, pp.pragmas)
  let retC = if pp.ret == nil or isDot(pp.ret): "void" else: genType(pp.ret)
  var args: seq[string] = @[]
  for pn in pp.pnodes:
    let nameNode = pn.kids[0]
    let pt = paramType(pn)
    if isList(pt) and pt.tag == "varargs":
      args.add "..."
    else:
      args.add declare(pt, mangleToC(nameNode.atom))
  var prefix = ""
  if pp.pragmas != nil and hasPragma(pp.pragmas, ["inline"]): prefix = "static inline "
  let argStr = if args.len > 0: joinSeq(args, ", ") else: "void"
  return prefix & retC & " " & name & "(" & argStr & ")"

proc genProc(p: Node): string =
  let pp = procParts(p)
  let sig = procSignature(p)
  if pp.body == nil: return sig & ";"
  return sig & " {\n" & genStmt(pp.body) & "\n}"

# ---------------------------------------------------------------------------
# literal-node check (for deciding inline global initializers)
# ---------------------------------------------------------------------------
proc isLiteralNode(v: Node): bool =
  if v == nil: return false
  if v.kind == nkStr or v.kind == nkChar: return true
  if isAtom(v):
    let a = v.atom
    return a == "." or a == "true" or a == "false" or a == "nil" or
      isIntLit(a) or isUIntLit(a) or isFloatLit(a)
  if isList(v):
    if oneOf(v.tag, ["suf", "true", "false", "nil", "inf", "neginf", "nan", "cast", "conv"]):
      return true
    if v.tag == "aconstr" or v.tag == "oconstr":
      var i = 1
      while i < v.kids.len:
        let k = v.kids[i]
        if isList(k) and k.tag == "kv":
          if not isLiteralNode(k.kids[1]): return false
        else:
          if not isLiteralNode(k): return false
        inc i
      return true
  return false

