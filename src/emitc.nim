proc genConstr(e: Node): string =
  if e.tag == "aconstr":
    var vals: seq[string] = @[]
    var i = 1
    while i < e.kids.len:
      vals.add genExpr(e.kids[i])
      inc i
    if isAtom(e.kids[0]):
      return "(" & genType(e.kids[0]) & "){ .a = { " & joinSeq(vals, ", ") & " } }"
    return "{ " & joinSeq(vals, ", ") & " }"
  let ty = genType(e.kids[0])
  var parts: seq[string] = @[]
  var i = 1
  while i < e.kids.len:
    let kv = e.kids[i]
    if isList(kv) and kv.tag == "kv":
      let fld = kv.kids[0]
      let fname = if isAtom(fld): mangleToC(fld.atom) else: genExpr(fld)
      var inh = ""
      if kv.kids.len > 2 and isAtom(kv.kids[2]) and isIntLit(kv.kids[2].atom):
        inh = myRepeat(".Q", parseI64(kv.kids[2].atom).int)
      parts.add inh & "." & fname & " = " & genExpr(kv.kids[1])
    else:
      parts.add genExpr(kv)
    inc i
  return "(" & ty & "){ " & joinSeq(parts, ", ") & " }"

proc genExpr(e: Node): string =
  if e == nil: return ""
  if e.kind == nkStr: return makeCString(e.strVal)
  if e.kind == nkChar: return "(NC8)" & toCCharLit(e.chrVal)
  if isAtom(e):
    let a = e.atom
    if a == "true": return "NIM_TRUE"
    if a == "false": return "NIM_FALSE"
    if a == "nil": return "NIM_NIL"
    if a == "inf": return "INF"
    if a == "neginf": return "-INF"
    if a == "nan": return "NAN"
    if isUIntLit(a): return genUIntLit(a)
    if isIntLit(a): return genIntLit(a)
    if isFloatLit(a): return a
    return symName(a)
  if not isList(e):
    fail("aowlc: bad expr node")
    return ""
  let t = e.tag
  let tb = typedBinOp(t)
  if tb.len > 0:
    let ty = genType(e.kids[0])
    let a = e.kids[e.kids.len - 2]
    let b = e.kids[e.kids.len - 1]
    return "((" & ty & ")(" & genExpr(a) & tb & genExpr(b) & "))"
  let cb = cmpBinOp(t)
  if cb.len > 0:
    let a = e.kids[e.kids.len - 2]
    let b = e.kids[e.kids.len - 1]
    return "(" & genExpr(a) & cb & genExpr(b) & ")"
  if t == "neg":
    return "((" & genType(e.kids[0]) & ")-" & genExpr(e.kids[e.kids.len - 1]) & ")"
  elif t == "bitnot":
    return "((" & genType(e.kids[0]) & ")~" & genExpr(e.kids[e.kids.len - 1]) & ")"
  elif t == "not":
    return "(!" & genExpr(e.kids[e.kids.len - 1]) & ")"
  elif t == "cast" or t == "conv" or t == "hconv":
    return "((" & genType(e.kids[0]) & ")" & genExpr(e.kids[e.kids.len - 1]) & ")"
  elif t == "baseobj":
    let expr = genExpr(e.kids[e.kids.len - 1])
    var level = 1
    if e.kids.len >= 3 and isAtom(e.kids[1]) and isIntLit(e.kids[1].atom):
      level = parseI64(e.kids[1].atom).int
    return "(" & expr & myRepeat(".Q", level) & ")"
  elif t == "call" or t == "hcall" or t == "onerr":
    var start = 1
    if t == "onerr": start = 2
    let fn = genExpr(e.kids[start - 1])
    var args: seq[string] = @[]
    var i = start
    while i < e.kids.len:
      args.add genExpr(e.kids[i])
      inc i
    return fn & "(" & joinSeq(args, ", ") & ")"
  elif t == "suf":
    let val = e.kids[0]
    let suf = e.kids[1]
    if val != nil and val.kind == nkStr: return genExpr(val)
    let sname = if isAtom(suf): suf.atom elif (suf != nil and suf.kind == nkStr): suf.strVal else: ""
    let ty = suffixType(sname)
    return "((" & ty & ")" & genExpr(val) & ")"
  elif t == "paren" or t == "expr":
    return "(" & genExpr(e.kids[e.kids.len - 1]) & ")"
  elif t == "addr":
    return "(&" & genExpr(e.kids[0]) & ")"
  elif t == "deref" or t == "hderef":
    return "(*" & genExpr(e.kids[0]) & ")"
  elif t == "at":
    return genExpr(e.kids[0]) & ".a[" & genExpr(e.kids[1]) & "]"
  elif t == "pat":
    return genExpr(e.kids[0]) & "[" & genExpr(e.kids[1]) & "]"
  elif t == "dot":
    let obj = genExpr(e.kids[0])
    let fld = e.kids[1]
    var inh = ""
    if e.kids.len > 2 and isAtom(e.kids[2]) and isIntLit(e.kids[2].atom):
      inh = myRepeat(".Q", parseI64(e.kids[2].atom).int)
    let fname = if isAtom(fld): mangleToC(fld.atom) else: genExpr(fld)
    return obj & inh & "." & fname
  elif t == "sizeof":
    return "sizeof(" & genType(e.kids[0]) & ")"
  elif t == "alignof":
    return "NIM_ALIGNOF(" & genType(e.kids[0]) & ")"
  elif t == "true": return "NIM_TRUE"
  elif t == "false": return "NIM_FALSE"
  elif t == "nil": return "NIM_NIL"
  elif t == "inf": return "INF"
  elif t == "neginf": return "-INF"
  elif t == "nan": return "NAN"
  elif t == "oconstr" or t == "aconstr": return genConstr(e)
  elif t == "ovf": return "LENGC_OVF_"
  else:
    fail("aowlc: unsupported expr '" & t & "'")
    return ""

# ---------------------------------------------------------------------------
# statements
# ---------------------------------------------------------------------------
proc genLvalue(n: Node): string =
  if isAtom(n): return symName(n.atom)
  return genExpr(n)

proc condExpr(n: Node): string =
  if isList(n) and cmpBinOp(n.tag).len > 0:
    let a = n.kids[n.kids.len - 2]
    let b = n.kids[n.kids.len - 1]
    return genExpr(a) & cmpBinOp(n.tag) & genExpr(b)
  return genExpr(n)

proc declName(name: string; pragmas: Node): string =
  let ext = externName(pragmas)
  if ext.len > 0: return ext
  if isList(pragmas) and hasPragma(pragmas, ["importc", "exportc", "importcpp"]):
    return beforeDot(name)
  return mangleToC(name)

proc genLocalVar(s: Node): string =
  let nameAtom = s.kids[0]
  let pragmas = s.kids[1]
  var typ = s.kids[2]
  let value = if s.kids.len > 3: s.kids[3] else: nil
  let isConst = s.tag == "const"
  let nm = if isAtom(nameAtom): declName(nameAtom.atom, pragmas) else: "v"
  if isDot(typ): typ = nil
  let hasInit = value != nil and not isDot(value)
  var decl = if typ != nil: declare(typ, nm) else: "NI " & nm
  if isConst: decl = "static const " & decl
  return decl & (if hasInit: " = " & genExpr(value) else: "") & ";"

proc genIf(s: Node): string =
  var outp = ""
  var firstElif = true
  for br in s.kids:
    if not isList(br): continue
    if br.tag == "elif":
      outp.add (if firstElif: "if (" else: " else if (") & condExpr(br.kids[0]) & ") {\n" &
        genStmt(br.kids[1]) & "\n}"
      firstElif = false
    elif br.tag == "else":
      outp.add " else {\n" & genStmt(br.kids[0]) & "\n}"
  return outp

proc genCase(s: Node): string =
  var outp = "switch (" & condExpr(s.kids[0]) & ") {\n"
  var i = 1
  while i < s.kids.len:
    let br = s.kids[i]
    inc i
    if not isList(br): continue
    if br.tag == "of":
      let ranges = br.kids[0]
      let body = br.kids[1]
      var labels: seq[string] = @[]
      var vals: seq[Node] = @[]
      if isList(ranges) and ranges.tag == "ranges":
        for k in ranges.kids: vals.add k
      else:
        vals.add ranges
      for v in vals:
        if isList(v) and v.tag == "range":
          labels.add "case " & genExpr(v.kids[0]) & " ... " & genExpr(v.kids[1]) & ":"
        else:
          labels.add "case " & genExpr(v) & ":"
      outp.add joinSeq(labels, "\n") & " {\n" & genStmt(body) & "\nbreak;\n}\n"
    elif br.tag == "else":
      outp.add "default: {\n" & genStmt(br.kids[0]) & "\nbreak;\n}\n"
  return outp & "}"

proc genKeepOverflow(s: Node): string =
  let op = s.kids[0]
  let lval = s.kids[1]
  var builtin = ""
  if op.tag == "add": builtin = "__builtin_add_overflow"
  elif op.tag == "sub": builtin = "__builtin_sub_overflow"
  elif op.tag == "mul": builtin = "__builtin_mul_overflow"
  if builtin.len == 0:
    fail("aowlc: unsupported keepovf op '" & op.tag & "'")
    return ""
  let a = op.kids[op.kids.len - 2]
  let b = op.kids[op.kids.len - 1]
  return "if (" & builtin & "(" & genExpr(a) & ", " & genExpr(b) & ", &" &
    genLvalue(lval) & ")) { LENGC_OVF_ = LENGC_OVF_ || NIM_TRUE; }"

proc genStmt(s: Node): string =
  if isDot(s): return ""
  if not isList(s): return ""
  let t = s.tag
  if t == "stmts":
    var parts: seq[string] = @[]
    for k in s.kids:
      let c = genStmt(k)
      if c.len > 0: parts.add c
    return joinSeq(parts, "\n")
  elif t == "scope":
    var parts: seq[string] = @[]
    for k in s.kids:
      let c = genStmt(k)
      if c.len > 0: parts.add c
    return "{\n" & joinSeq(parts, "\n") & "\n}"
  elif t == "var" or t == "let" or t == "cursor" or t == "const" or
       t == "gvar" or t == "glet" or t == "tvar":
    return genLocalVar(s)
  elif t == "asgn":
    return genLvalue(s.kids[0]) & " = " & genExpr(s.kids[1]) & ";"
  elif t == "store":
    return genLvalue(s.kids[1]) & " = " & genExpr(s.kids[0]) & ";"
  elif t == "ret":
    if s.kids.len == 0 or isDot(s.kids[0]): return "return;"
    return "return " & genExpr(s.kids[0]) & ";"
  elif t == "if":
    return genIf(s)
  elif t == "while":
    return "while (" & condExpr(s.kids[0]) & ") {\n" & genStmt(s.kids[1]) & "\n}"
  elif t == "loop":
    let pre = genStmt(s.kids[0])
    let cond = genExpr(s.kids[1])
    let body = genStmt(s.kids[2])
    return "while (NIM_TRUE) {\n" & pre & "\nif (!(" & cond & ")) break;\n" & body & "\n}"
  elif t == "break": return "break;"
  elif t == "continue": return "continue;"
  elif t == "call" or t == "hcall": return genExpr(s) & ";"
  elif t == "onerr": return genExpr(s) & ";"
  elif t == "discard": return "(void)(" & genExpr(s.kids[0]) & ");"
  elif t == "lab":
    let nm = if isAtom(s.kids[0]): mangleToC(s.kids[0].atom) else: "L"
    return nm & ": ;"
  elif t == "jmp":
    let nm = if isAtom(s.kids[0]): mangleToC(s.kids[0].atom) else: "L"
    return "goto " & nm & ";"
  elif t == "case":
    return genCase(s)
  elif t == "keepovf":
    return genKeepOverflow(s)
  elif t == "raise":
    return "/* raise */ (void)0;"
  elif t == "pragmas" or t == "comment" or t == "emit" or t == "smry":
    return ""
  else:
    fail("aowlc: unsupported stmt '" & t & "'")
    return ""

