# type declarations
# ---------------------------------------------------------------------------
proc genObjectFields(body: Node): string =
  var fields = ""
  var start = 0
  if body.kids.len > 0 and (isDot(body.kids[0]) or isAtom(body.kids[0])):
    if isAtom(body.kids[0]) and not isDot(body.kids[0]):
      fields.add typeName(body.kids[0].atom) & " Q;\n"
    start = 1
  var i = start
  while i < body.kids.len:
    let f = body.kids[i]
    inc i
    if not isList(f): continue
    if f.tag == "fld":
      let fn = f.kids[0]
      let ftype = f.kids[f.kids.len - 1]
      fields.add declare(ftype, mangleToC(fn.atom)) & ";\n"
    elif f.tag == "union":
      fields.add "union {\n"
      for branch in f.kids:
        if isList(branch) and branch.tag == "object":
          fields.add "struct {\n" & genObjectFields(branch) & "};\n"
      fields.add "};\n"
  return fields

proc genObjectDecl(nm: string; body: Node; isUnion: bool): string =
  let kw = if isUnion: "typedef union " else: "typedef struct "
  return kw & nm & " {\n" & genObjectFields(body) & "} " & nm & ";"

proc genEnumDecl(nm: string; body: Node): string =
  let base = body.kids[0]
  var outp = "typedef " & genType(base) & " " & nm & ";\n"
  var i = 1
  while i < body.kids.len:
    let ef = body.kids[i]
    inc i
    if isList(ef) and ef.tag == "efld":
      let en = ef.kids[0]
      let ev = ef.kids[1]
      outp.add "#define " & mangleToC(en.atom) & " ((" & genType(base) & ")" & genExpr(ev) & ")\n"
  return outp

proc genTypeDecl(td: Node): string =
  let nameAtom = td.kids[0]
  let pragmas = findTag(td, "pragmas")
  let body = td.kids[td.kids.len - 1]
  if hasPragma(pragmas, ["nodecl", "importc", "importcpp", "header"]): return ""
  let nm = mangleToC(nameAtom.atom)
  if isList(body):
    if body.tag == "object" or body.tag == "union":
      return genObjectDecl(nm, body, body.tag == "union")
    if body.tag == "enum":
      return genEnumDecl(nm, body)
    if body.tag == "array":
      let size = body.kids[body.kids.len - 1]
      let len2 = if isAtom(size): size.atom else: "0"
      return "typedef struct " & nm & " { " & genType(body.kids[0]) & " a[" & len2 & "]; } " & nm & ";"
    if body.tag == "proctype":
      return "typedef " & declare(body, nm) & ";"
    if oneOf(body.tag, ["i", "u", "f", "c", "bool", "ptr", "aptr"]):
      return "typedef " & declare(body, nm) & ";"
  if isAtom(body):
    return "typedef " & typeName(body.atom) & " " & nm & ";"
  return ""

