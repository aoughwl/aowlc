var modTypeVals: seq[Node] = @[]

proc resetEmitter() =
  externKeys = @[]
  externVals = @[]
  typeExternKeys = @[]
  typeExternVals = @[]
  noDeclTypes = @[]
  ownMod = prog.main.name
  loadedMods = @[]
  siblingSuffixes = @[]
  siblingSuffixesLoaded = false
  modProcKeys = @[]
  modProcVals = @[]
  modGlobalKeys = @[]
  modGlobalVals = @[]
  modTypeKeys = @[]
  modTypeVals = @[]

proc mapHas(keys: seq[string]; k: string): bool =
  for x in keys:
    if x == k: return true
  return false

proc mapGet(keys, vals: seq[string]; k: string): string =
  for i in 0 ..< keys.len:
    if keys[i] == k: return vals[i]
  return ""

proc symName(name: string): string =
  if mapHas(externKeys, name): return mapGet(externKeys, externVals, name)
  return mangleToC(name)

proc typeName(name: string): string =
  if mapHas(typeExternKeys, name): return mapGet(typeExternKeys, typeExternVals, name)
  return mangleToC(name)

# ---------------------------------------------------------------------------
# pragma helpers
# ---------------------------------------------------------------------------
proc externName(pragmas: Node): string =
  if not isList(pragmas) or pragmas.tag != "pragmas": return ""
  for p in pragmas.kids:
    if isList(p) and (p.tag == "importc" or p.tag == "exportc" or p.tag == "importcpp"):
      if p.kids.len > 0:
        let s = p.kids[0]
        if s != nil and s.kind == nkStr: return s.strVal
        if isAtom(s): return s.atom
      return ""
  return ""

proc hasPragma(pragmas: Node; names: openArray[string]): bool =
  if not isList(pragmas) or pragmas.tag != "pragmas": return false
  for p in pragmas.kids:
    if isList(p) and oneOf(p.tag, names): return true
  return false

